public class OperadoresTernario {
    // Os operadores aritméticos são: adição(+), subtração(-), multiplicação(*), divisão(/).
    public static void main(String[] args) {
        
        // Operador ternário

        int a, b;

        a = 4;
        b = 8;
        String resultado = a==b ?"verdadeiro" : "falso";
        
        System.out.println(resultado);

    }
}
